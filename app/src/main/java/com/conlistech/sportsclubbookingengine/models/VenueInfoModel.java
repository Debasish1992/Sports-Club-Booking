package com.conlistech.sportsclubbookingengine.models;

public class VenueInfoModel {

    String address, amenities,
            owner_name, venue_email,
            venue_equipments, venue_image,
            venue_name, venue_offers,
            venue_phone, venue_website;


    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        address = address;
    }

    public String getAmenities() {
        return amenities;
    }

    public void setAmenities(String amenities) {
        this.amenities = amenities;
    }

    public String getOwner_name() {
        return owner_name;
    }

    public void setOwner_name(String owner_name) {
        this.owner_name = owner_name;
    }

    public String getVenue_email() {
        return venue_email;
    }

    public void setVenue_email(String venue_email) {
        this.venue_email = venue_email;
    }

    public String getVenue_equipments() {
        return venue_equipments;
    }

    public void setVenue_equipments(String venue_equipments) {
        this.venue_equipments = venue_equipments;
    }

    public String getVenue_image() {
        return venue_image;
    }

    public void setVenue_image(String venue_image) {
        this.venue_image = venue_image;
    }

    public String getVenue_name() {
        return venue_name;
    }

    public void setVenue_name(String venue_name) {
        this.venue_name = venue_name;
    }

    public String getVenue_offers() {
        return venue_offers;
    }

    public void setVenue_offers(String venue_offers) {
        this.venue_offers = venue_offers;
    }

    public String getVenue_phone() {
        return venue_phone;
    }

    public void setVenue_phone(String venue_phone) {
        this.venue_phone = venue_phone;
    }

    public String getVenue_website() {
        return venue_website;
    }

    public void setVenue_website(String venue_website) {
        this.venue_website = venue_website;
    }
}
